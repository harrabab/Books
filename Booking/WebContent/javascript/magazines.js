// Returns the HTML for the content not found
const getNotFoundHTML = () => {
    let headerContainer = document.createElement('h3');
    headerContainer.className = "no-content";
    headerContainer.textContent = "No Content found"
    return headerContainer;
}

/* Alters the header image and filter for every magazine url change
 PARAMETERS
 targetId : Magazine ID in the window URL
*/
const alterHeaderAndFilter = (targetId) => {
    if (document.getElementsByClassName("magazine-cover-header")[0]) {
        document.getElementsByClassName("magazine-cover-header")[0].style.display = targetId ? "none" : "flex";
    }
    const headerElement = document.getElementsByClassName("header-show")[0];
    if (headerElement) {
        headerElement.className = targetId ? "header-show header-hide" : "header-show";
    }
    if (targetId) {
        window.scrollTo(0, 0);
    }
}

/* Return complete HTML code for the magazine content
 PARAMETERS
 data : Complete magazines data
*/
const getMagazineCards = (data) => {
    const targetId = getParameterByName('id');
    alterHeaderAndFilter(targetId);
    let htmlCode = document.createElement('div');
    if (!targetId) {
        for (let i = 0; i < data.length; i++) {
            htmlCode.appendChild(getMagazineCardHTML(data[i]));
        }
    }
    else {
        const content = data.filter(item => { if (item.id == targetId) { return item } })[0];
        if (content) {
            htmlCode.appendChild(getMagazineDetailHTML(content))
        }
        else {
            return getNotFoundHTML();
        }
    }

    return htmlCode;
}

// Update call for every change in the HTML dom
const updateContent = () => {
    const mainContainer = document.getElementsByClassName("main-container")[0];
    if (mainContainer) {
        mainContainer.innerHTML = "";
    }
    const cardsHtml = getMagazineCards(data);
    if (mainContainer) {
        mainContainer.appendChild(cardsHtml)
    }
}

