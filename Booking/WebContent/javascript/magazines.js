/* Read More button on click handler
 PARAMETERS
 id = magazine id
 */
const onClickReadMore = (id) => {
    window.location.href = "magazines.html?id=" + id
}

/* Returns the id paramter from the url
 PARAMETERS
 name : paramter name
 url : on which url the operation has to be performed
*/
const getParameterByName = (name, url) => {
    if (!url) url = window.location.href;
    name = name.replace(/[\[\]]/g, "\\$&");
    var regex = new RegExp("[?&]" + name + "(=([^&#]*)|&|#|$)"),
        results = regex.exec(url);
    if (!results) return null;
    if (!results[2]) return '';
    return decodeURIComponent(results[2].replace(/\+/g, " "));
}

/* Returns HTML code for the home page of magazine
 PARAMETERS
 content : magazine object
*/
const getMagazineCardHTML = (content) => {
    let container = document.createElement('div');
    container.className = "magazine-tile";
    let contentContainer = document.createElement('div');
    contentContainer.className = "content";

    let magazineImageContainer = document.createElement("div");
    magazineImageContainer.className = "magazine-icon content-image";
    magazineImageContainer.style.backgroundImage = `url(${content.imageUrl})`

    let cardText = document.createElement('p');
    cardText.className = "magazine-header"
    cardText.textContent = content.title.substr(0, 50);

    let readMoreContainer = document.createElement("div");
    readMoreContainer.className = "read-more-container";

    let clapsImage = document.createElement("img");
    clapsImage.src = "https://image.flaticon.com/icons/svg/599/599730.svg";
    clapsImage.alt = "claps"
    clapsImage.className = "heart-icon"

    let numberOfLikes = document.createElement('span');
    numberOfLikes.textContent = content.liked;

    let readMoreButton = document.createElement("button");
    readMoreButton.className = "custom-btn";
    readMoreButton.textContent = "READ MORE";
    readMoreButton.onclick = () => { onClickReadMore(content.id) };

    contentContainer.appendChild(magazineImageContainer);

    readMoreContainer.appendChild(clapsImage);
    readMoreContainer.appendChild(numberOfLikes);
    readMoreContainer.appendChild(readMoreButton);

    container.appendChild(contentContainer);
    container.appendChild(cardText);
    container.appendChild(readMoreContainer);

    return container;
}


/* Returns comments HTML code for magazine inner page
 PARAMETERS
 comments : comment array for a particular magazine article
*/
const getCommentsHTML = (comments) => {
    let commentsMainContainer = document.createElement('div')
    commentsMainContainer.className = "comment-main-container";
    for (let comment of comments) {
        let commentsContainer = document.createElement('div');
        commentsContainer.className = "comment-container";

        let commentProfileContainer = document.createElement('div');
        commentProfileContainer.className = "comment-dot";

        let commentProfileIcon = document.createElement("img");
        commentProfileIcon.src = comment.picture;
        commentProfileIcon.alt = "icon"
        commentProfileIcon.className = "comment-icon"

        let commenterName = document.createElement("div");
        commenterName.className = "comment-name";
        commenterName.textContent = comment.name;

        let commenterComment = document.createElement("div");
        commenterComment.className = "comment-message";
        commenterComment.textContent = comment.comment;

        commentProfileContainer.appendChild(commentProfileIcon);

        commentsContainer.appendChild(commentProfileContainer);
        commentsContainer.appendChild(commenterName);
        commentsContainer.appendChild(commenterComment);

        commentsMainContainer.appendChild(commentsContainer);
    }
    return commentsMainContainer;
}

/* Returns the magazine inner page HTML
 PARAMETERS
 content : Particular magazine article object
*/
