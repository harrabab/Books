    magazineImage.style.backgroundImage = `url(${content.imageUrl})`

    let detailDescription = document.createElement('div');
    detailDescription.className = "detail-description";
    detailDescription.textContent = content.description;

    let clapsContainer = document.createElement('div');
    clapsContainer.className = "detail-description  heart-float";

    let clapsImage = document.createElement("img");
    clapsImage.src = "https://image.flaticon.com/icons/svg/599/599730.svg";
    clapsImage.alt = "claps"
    clapsImage.className = "heart-inner-icon"

    let numberOfLikes = document.createElement('span');
    numberOfLikes.textContent = content.liked;

    let commentsHeader = document.createElement('div');
    commentsHeader.className = "comments-header";
    commentsHeader.textContent = content.comments.length + " Comments";

    clapsContainer.appendChild(clapsImage);
    clapsContainer.appendChild(numberOfLikes);

    container.appendChild(headerText);
    container.appendChild(magazineImage);
    container.appendChild(detailDescription);
    container.appendChild(clapsContainer);
    container.appendChild(commentsHeader);
    container.appendChild(getCommentsHTML(content.comments));
    return container;
